#include <iostream>
#include <cmath>
using namespace std;
int main(){
	
double x, y;
	x=6;
	y=2;
	cout<<"!(x>2)        = "<<!(x>2)<<endl;
	cout<<"(x>y)&& (y>0) = "<<(x>y)&& (y>0);
	cout<<"\n";
	cout<<"(x<y)&&(y>0   = "<<(x<y)&&(y>0);
	cout<<"\n";
	cout<<"(x<y)||(y>0)  = "<<(x<y)||(y>0);	
	
	
	
	return 0;
}
