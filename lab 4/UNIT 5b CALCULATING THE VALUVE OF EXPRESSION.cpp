#include <iostream>
#include <cmath>
using namespace std;
int main(){
	//Calculating the value of the expressions.
	cout<<"Expression           Value\n\n";
	
	cout<< "28/4-2         =       " << 28/4-2 <<endl;
	cout<< "6+12*2-8       =       " <<6+12*2-8 <<endl;
	cout<< "4+8*2          =       " << 4+8*2 <<endl;
	cout<< "6+17%3-2       =       " << 6+17%3-2 <<endl;
	cout<< "2+22*(9-7)     =       " << 2+22*(9-7) <<endl;
	cout<< "(8+7)*2        =       " << (8+7)*2 <<endl;
	cout<< "(16+7)%2-1     =       " <<(16+7)%2-1 <<endl;
	cout<< "12/(10-6)      =       " << 12/(10-6) <<endl;	
	cout<< "(19-3)*(2+2)/4 =       " << (19-3)*(2+2)/4 <<endl;		
	
	
	
	return 0;
}